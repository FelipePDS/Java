    private void depositButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_depositButtonActionPerformed
        Usuario usuario = new Usuario();
        
        if (Float.parseFloat(deposit.getText()) >= 10) {
            Float previousBalance = usuario.getBalance();
            usuario.setBalance(usuario.getBalance() + Float.parseFloat(deposit.getText()));
            
            JOptionPane.showMessageDialog(null, "Saldo Anterior: R$ " + previousBalance + "\nSaldo Atual: R$ " + usuario.getBalance());
            System.exit(0);
        } else {
            JOptionPane.showMessageDialog(null, "O Dep√≥sito tem que ser acima de R$ 10.00");
        }
    }//GEN-LAST:event_depositButtonActionPerformed


